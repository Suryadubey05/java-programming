package do_while;

import java.util.Scanner;

public class que49 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int in;
        do {
            System.out.println("hello");
            System.out.println("enter any num");
            in = sc.nextInt();
        } while (in != 0);
    }

}

